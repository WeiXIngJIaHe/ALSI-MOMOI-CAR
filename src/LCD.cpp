/* LCD1602 — PCF8574 + HD44780 字符液晶                               */
/* ================================================================ */

static const char *TAG_LCD = "LCD1602";

LCD1602::LCD1602(uint8_t addr, uint8_t cols, uint8_t rows)
    : _addr(addr), _cols(cols), _rows(rows),
      _backlight(LCD_FLAG_DISPLAY_ON),
      _display_ctrl(LCD_FLAG_DISPLAY_ON),
      _entry_mode(LCD_FLAG_ENTRY_LEFT)
{}

void LCD1602::_delayUs(uint32_t us)
{
    if (us < 1000) {
        ets_delay_us(us);
    } else {
        vTaskDelay(pdMS_TO_TICKS(us / 1000));
    }
}

void LCD1602::_i2c_write(uint8_t val)
{
    uint8_t data = val | _backlight;
    i2c_master_write_to_device(I2C_NUM_0, _addr, &data, 1, pdMS_TO_TICKS(10));
}

void LCD1602::_pulse_enable(uint8_t data)
{
    _i2c_write(data | (1 << 2));
    _delayUs(1);
    _i2c_write(data & ~(1 << 2));
    _delayUs(50);
}

void LCD1602::_write4bit(uint8_t data)   { _pulse_enable(data); }

void LCD1602::_write8bit(uint8_t data, bool rs)
{
    uint8_t hi = (data & 0xF0);
    uint8_t lo = (data & 0x0F) << 4;
    if (rs) hi |= 1;
    _write4bit(hi);
    if (lo) _write4bit(lo | (rs ? 1 : 0));
}

void LCD1602::init()
{
    _delayUs(50000);
    _write4bit(0x30); _delayUs(4500);
    _write4bit(0x30); _delayUs(150);
    _write4bit(0x30); _delayUs(150);
    _write4bit(0x20);

    _write8bit(LCD_CMD_FUNCTION_SET | LCD_FLAG_2LINE, false); _delayUs(50);
    _write8bit(LCD_CMD_DISPLAY_CTRL | LCD_FLAG_DISPLAY_ON, false); _delayUs(50);
    clear();
    _write8bit(LCD_CMD_ENTRY_MODE | LCD_FLAG_ENTRY_LEFT, false); _delayUs(50);

    ESP_LOGI(TAG_LCD, "OK (0x%02X, %dx%d)", _addr, _cols, _rows);
}

void LCD1602::clear()         { _write8bit(LCD_CMD_CLEAR, false); _delayUs(2000); }
void LCD1602::home()          { _write8bit(LCD_CMD_HOME, false);  _delayUs(2000); }

void LCD1602::setCursor(uint8_t col, uint8_t row)
{
    static const uint8_t off[] = { 0x00, 0x40, 0x14, 0x54 };
    col = (col < _cols) ? col : _cols - 1;
    row = (row < _rows) ? row : _rows - 1;
    _write8bit(LCD_CMD_SET_DDRAM | (col + off[row]), false);
    _delayUs(50);
}

void LCD1602::write(const char *str)      { while (*str) _write8bit(*str++, true); }
void LCD1602::writeChar(char c)           { _write8bit(c, true); }

void LCD1602::backlight(bool on)          { _backlight = on ? (1 << 3) : 0; _i2c_write(0x00); }

void LCD1602::display(bool on)
{
    if (on) _display_ctrl |=  LCD_FLAG_DISPLAY_ON;
    else    _display_ctrl &= ~LCD_FLAG_DISPLAY_ON;
    _write8bit(LCD_CMD_DISPLAY_CTRL | _display_ctrl, false);
}

void LCD1602::cursor(bool on)
{
    if (on) _display_ctrl |=  LCD_FLAG_CURSOR_ON;
    else    _display_ctrl &= ~LCD_FLAG_CURSOR_ON;
    _write8bit(LCD_CMD_DISPLAY_CTRL | _display_ctrl, false);
}

void LCD1602::blink(bool on)
{
    if (on) _display_ctrl |=  LCD_FLAG_BLINK_ON;
    else    _display_ctrl &= ~LCD_FLAG_BLINK_ON;
    _write8bit(LCD_CMD_DISPLAY_CTRL | _display_ctrl, false);
}

