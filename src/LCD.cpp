SSD1306::SSD1306(uint8_t addr) : _addr(addr)  { clear(); }

void SSD1306::_sendCmd(uint8_t cmd)
{
    uint8_t buf[2] = { 0x00, cmd };
    i2c_master_write_to_device(I2C_NUM_0, _addr, buf, 2, pdMS_TO_TICKS(10));
}

void SSD1306::_sendData(const uint8_t *data, size_t len)
{
    uint8_t buf[33];
    buf[0] = 0x40;
    while (len > 0) {
        size_t chunk = (len > 32) ? 32 : len;
        std::memcpy(buf + 1, data, chunk);
        i2c_master_write_to_device(I2C_NUM_0, _addr, buf, chunk + 1, pdMS_TO_TICKS(20));
        data += chunk; len -= chunk;
    }
}

void SSD1306::init()
{
    _sendCmd(OLED_CMD_DISPLAY_OFF);
    _sendCmd(OLED_CMD_MEMORY_MODE); _sendCmd(0x00);
    _sendCmd(OLED_CMD_START_LINE | 0x00);
    _sendCmd(0x40);
    _sendCmd(0xD3); _sendCmd(0x00);
    _sendCmd(0xA8); _sendCmd(OLED_HEIGHT - 1);
    _sendCmd(0xA1); _sendCmd(0xC8);
    _sendCmd(0xDA); _sendCmd(0x12);
    _sendCmd(0xD5); _sendCmd(0x80);
    _sendCmd(0xD9); _sendCmd(0xF1);
    _sendCmd(0xDB); _sendCmd(0x30);
    _sendCmd(0x8D); _sendCmd(0x14);
    _sendCmd(0xA4);
    setContrast(0x7F);
    _sendCmd(OLED_CMD_DISPLAY_ON);
    clear(); display();
    ESP_LOGI(TAG_OLED, "OK (0x%02X, %dx%d)", _addr, OLED_WIDTH, OLED_HEIGHT);
}

void SSD1306::clear()          { std::memset(_buf, 0, sizeof(_buf)); }

void SSD1306::display()
{
    _sendCmd(OLED_CMD_COL_ADDR); _sendCmd(0); _sendCmd(OLED_WIDTH - 1);
    _sendCmd(OLED_CMD_PAGE_ADDR); _sendCmd(0); _sendCmd(OLED_PAGES - 1);
    _sendData(_buf, sizeof(_buf));
}

void SSD1306::setPixel(uint8_t x, uint8_t y, bool on)
{
    if (x >= OLED_WIDTH || y >= OLED_HEIGHT) return;
    if (on) _buf[x + (y / 8) * OLED_WIDTH] |=  (1 << (y & 7));
    else    _buf[x + (y / 8) * OLED_WIDTH] &= ~(1 << (y & 7));
}

void SSD1306::drawLine(uint8_t x0, uint8_t y0, uint8_t x1, uint8_t y1)
{
    int dx = abs((int)x1 - (int)x0), dy = -abs((int)y1 - (int)y0);
    int sx = x0 < x1 ? 1 : -1, sy = y0 < y1 ? 1 : -1;
    int err = dx + dy, e2;
    while (1) {
        setPixel(x0, y0, true);
        if (x0 == x1 && y0 == y1) break;
        e2 = 2 * err;
        if (e2 >= dy) { err += dy; x0 += sx; }
        if (e2 <= dx) { err += dx; y0 += sy; }
    }
}

void SSD1306::drawRect(uint8_t x, uint8_t y, uint8_t w, uint8_t h, bool fill)
{
    if (fill) {
        for (uint8_t i = 0; i < h; i++)
            for (uint8_t j = 0; j < w; j++)
                setPixel(x + j, y + i, true);
    } else {
        drawLine(x, y, x + w - 1, y); drawLine(x, y, x, y + h - 1);
        drawLine(x + w - 1, y, x + w - 1, y + h - 1);
        drawLine(x, y + h - 1, x + w - 1, y + h - 1);
    }
}

void SSD1306::drawCircle(uint8_t cx, uint8_t cy, uint8_t r, bool fill)
{
    int16_t f = 1 - r, x = 0, y = r;
    if (fill) for (int16_t i = y; i >= 0; i--) drawLine(cx - i, cy, cx + i, cy);
    setPixel(cx, cy + r, true); setPixel(cx, cy - r, true);
    setPixel(cx + r, cy, true); setPixel(cx - r, cy, true);
    while (x < y) {
        if (f >= 0) { y--; f += (2 * y); }
        x++; f += (2 * x - 1);
        setPixel(cx + x, cy + y, true); setPixel(cx - x, cy + y, true);
        setPixel(cx + x, cy - y, true); setPixel(cx - x, cy - y, true);
        setPixel(cx + y, cy + x, true); setPixel(cx - y, cy + x, true);
        setPixel(cx + y, cy - x, true); setPixel(cx - y, cy - x, true);
    }
}

void SSD1306::drawChar(uint8_t x, uint8_t y, char c)
{
    if (c < 0x20 || c > 0x7E) c = ' ';
    uint8_t idx = c - 0x20;
    for (uint8_t col = 0; col < 6; col++)
        for (uint8_t row = 0; row < 8; row++)
            setPixel(x + col, y + row, _font6x8[idx][col] & (1 << row));
}

void SSD1306::drawString(uint8_t x, uint8_t y, const char *str)
{
    while (*str) { drawChar(x, y, *str); x += 6; if (x + 6 > OLED_WIDTH) { x = 0; y += 8; } str++; }
}

void SSD1306::drawStringCenter(uint8_t y, const char *str)
{
    uint8_t len = strlen(str), x = (OLED_WIDTH - len * 6) / 2;
    drawString(x > OLED_WIDTH ? 0 : x, y, str);
}

void SSD1306::drawBitmap(uint8_t x, uint8_t y, const uint8_t *bmp, uint8_t w, uint8_t h)
{
    for (uint8_t r = 0; r < h; r++)
        for (uint8_t c = 0; c < w; c++)
            setPixel(x + c, y + r, bmp[(r * w + c) / 8] & (1 << (c & 7)));
}

void SSD1306::setContrast(uint8_t val)  { _sendCmd(OLED_CMD_SET_CONTRAST); _sendCmd(val); }
void SSD1306::invert(bool en)           { _sendCmd(en ? OLED_CMD_INVERT_DISP : OLED_CMD_NORMAL_DISP); }
void SSD1306::sleep(bool en)            { _sendCmd(en ? OLED_CMD_DISPLAY_OFF : OLED_CMD_DISPLAY_ON); }
